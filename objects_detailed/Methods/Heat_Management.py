def battery_heat_dissipation():
    I = 2.36 # Current in Amperes
    ACIR = 0.020 # Average Current Internal Resistance in Ohms
    DCIR = ACIR / 0.7 # DC Internal Resistance in Ohms
    P = I**2 * DCIR # Power dissipated as heat in Watts
    return P

def battery_efficiency():
    I = 2.36 # Current in Amperes
    V = 3.45 # Average charge voltage in Volts
    ACIR = 0.020 # Average Current Internal Resistance in Ohms
    DCIR = ACIR / 0.7 # DC Internal Resistance in Ohms
    delta_V = I * DCIR # Power loss due to internal resistance in Watts
    Average_charge_voltage = V + delta_V # Average charge voltage considering internal resistance in Volts
    Average_discharge_voltage = V - delta_V # Average discharge voltage considering internal resistance in Volts
    Efficiency = (Average_discharge_voltage / Average_charge_voltage) # Efficiency of the battery
    return Efficiency


