def number_batteries_required(Pow_req=1200.0):
    Pow_battery = 2.36 * 3.45 * battery_efficiency() # Power provided by each battery in Watts
    n_batteries_required = Pow_req / Pow_battery # Number of batteries required to meet the power requirement
    return n_batteries_required

def heat_conduction(Pow_req=1200.0, S=35.0):
    k = 0.017  # Thermal conductivity of used foam in W/(m*K)
    t = 0.03/2 # thickness of the aerogel in meters
    delta_T = 70.0  # Difference Temperature in Kelvin
    n_batteries = number_batteries_required(Pow_req) / 2 # Number of batteries per wingsection (assuming 2 sections)
    Q_battery = battery_heat_dissipation() * n_batteries # Heat generated by all batteries in Watts

    Q_heatengines = 2 * (k * S * 0.15 * delta_T / 4.5 ) / t # Heat transfer rate to account for by engines in worst case scenario in Watts

    Q_required = 2 * ((k * S * 0.15 * delta_T / 4.5 ) / t - Q_battery) # Required heat transfer rate in Watts

    return Q_required, Q_heatengines

